num=input("Enter a number") # Prompts the user to enter a number
num=int(num) #changes the number to an integer
if num % 2 == 0: #checks if the number is even or odd
    print(str(num) + " is even") #changes the int (num) into string and prints if num is even 
else :
    print(str(num) + " is odd")#changes the int (num) into string and prints prints if num is odd
